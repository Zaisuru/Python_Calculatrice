# Creation d'une calculatrice sur python en ligne de commande
PremierNombre = input("Veuillez entrer un premier nombre :")
DeuxiemeNombre = input("Veuillez entrer un deuxième nombre :")

print (f"Le résultat de l'addition du nombre {PremierNombre} avec le nombre {DeuxiemeNombre} est égal à {int(PremierNombre) + int(DeuxiemeNombre)}")